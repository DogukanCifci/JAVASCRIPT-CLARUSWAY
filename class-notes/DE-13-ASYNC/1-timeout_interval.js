const bekle = (ms) => {
  const start = new Date().getTime();

  while (new Date().getTime() < start + ms) {} //3 saniye bos döndürdü
};
console.time("timer1");
console.log("hello"); //Hemen yazar
bekle(3000);
console.timeEnd("timer1");
console.log("Naber"); //3 saniye sonra yazar
